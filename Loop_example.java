class ForLoopExample {
    public static void main(String[] args){
        for (int i=2; i<=5; i=i+2){
            System.out.println(i);
        }
    }
}

class ForLoopCountDown {
    public static void main (String[] argd ){
        for (int i = 5; i>=1; i--)
            System.out.println(i);
        }
}

class DowhileExample {
    public static void main(String[] args){
        int i = 5;
        do{
            System.out.println(i);
            i++;
       }while (i<=5);
    
    }
}